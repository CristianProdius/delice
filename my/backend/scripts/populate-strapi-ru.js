// ====== BLOG POSTS ======
// Upsert function for blog posts
async function upsertPost(postData) {
  try {
    // Check if post exists by slug
    const { data: existing } = await api.get(`/posts?filters[slug][$eq]=${postData.slug}&locale=${LOCALE}`);

    const payload = {
      data: {
        ...postData,
        locale: LOCALE,
        publishedAt: AUTO_PUBLISH ? new Date().toISOString() : null
      }
    };

    if (existing && existing.length > 0) {
      const id = existing[0].id;
      const res = await api.put(`/posts/${id}`, payload);
      return { id, action: '–æ–±–Ω–æ–≤–ª–µ–Ω–æ', data: res.data.data };
    } else {
      const res = await api.post('/posts', payload);
      return { id: res.data.data.id, action: '—Å–æ–∑–¥–∞–Ω–æ', data: res.data.data };
    }
  } catch (err) {
    console.error(`–û—à–∏–±–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –ø–æ—Å—Ç–∞ "${postData.title}":`, err?.response?.data || err.message);
    throw err;
  }
}

