// Upsert utility (find by slug+locale; create or update)
async function upsertPage({ title, slug, sections, seo }) {
  try {
    // Safer query with explicit ordering
    const q = `/pages?locale=${encodeURIComponent(LOCALE)}&filters[slug][$eq]=${encodeURIComponent(slug)}`;
    const found = await api.get(q).then(r => r.data?.data ?? []).catch(() => []);

    const payload = {
      data: {
        title,
        slug,
        locale: LOCALE,
        sections,
        seo,
        ...(AUTO_PUBLISH ? { publishedAt: new Date().toISOString() } : {})
      }
    };

    if (found.length > 0) {
      const id = found[0].id;
      const res = await api.put(`/pages/${id}`, payload);
      return { action: '–æ–±–Ω–æ–≤–ª–µ–Ω–æ', id: res.data.data.id, title };
    } else {
      const res = await api.post('/pages', payload);
      return { action: '—Å–æ–∑–¥–∞–Ω–æ', id: res.data.data.id, title };
    }
  } catch (error) {
    console.error(`‚ùå –ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–Ω–æ–≤–∏—Ç—å —Å—Ç—Ä–∞–Ω–∏—Ü—É "${title}":`, error.response?.data?.error || error.message);

    // Show detailed validation errors
    if (error.response?.data?.error?.details?.errors) {
      console.log('\nüìã –î–µ—Ç–∞–ª–∏ –æ—à–∏–±–æ–∫ –≤–∞–ª–∏–¥–∞—Ü–∏–∏:');
      error.response.data.error.details.errors.forEach((err, i) => {
        console.log(`   ${i + 1}. –ü–æ–ª–µ: ${err.path ? err.path.join('.') : '–Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ'}`);
        console.log(`      –û—à–∏–±–∫–∞: ${err.message}`);
        console.log(`      –ò–º—è: ${err.name || '–ù/–î'}`);
      });
      console.log('');
    }

    throw error;
  }
}

